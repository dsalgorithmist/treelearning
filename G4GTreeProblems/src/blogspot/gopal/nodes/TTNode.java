package blogspot.gopal.nodes;

/**
 * @author Gopal
 *
 */
public class TTNode {

	private int data;

	private TTNode left;

	private TTNode middle;

	private TTNode right;

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public TTNode getLeft() {
		return left;
	}

	public void setLeft(TTNode left) {
		this.left = left;
	}

	public TTNode getMiddle() {
		return middle;
	}

	public void setMiddle(TTNode middle) {
		this.middle = middle;
	}

	public TTNode getRight() {
		return right;
	}

	public void setRight(TTNode right) {
		this.right = right;
	}
}
